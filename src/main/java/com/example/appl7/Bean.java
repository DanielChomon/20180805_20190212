package com.example.appl7;

public class Bean {
    private String actorId;
    private String nombre;
    private int nCateogorias;
    private int nPeliculas;

    public String getActorId() {
        return actorId;
    }

    public void setActorId(String actorId) {
        this.actorId = actorId;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getnCateogorias() {
        return nCateogorias;
    }

    public void setnCateogorias(int nCateogorias) {
        this.nCateogorias = nCateogorias;
    }

    public int getnPeliculas() {
        return nPeliculas;
    }

    public void setnPeliculas(int nPeliculas) {
        this.nPeliculas = nPeliculas;
    }
}
